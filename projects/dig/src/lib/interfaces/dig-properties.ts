// standardized resource properties
export interface DigProperties {
  // app id
  id: string;

  // Text description, less than 50 characters
  description?: string;

  // App icon; can be a path to a file or one of the Ionic icons
  icon?: string;

  // App title, less than 20 characters
  title?: string;

  // status of project
  status?: string;
}


